package Lab5;

/**
 *
 * @author Thuy Linh
 */
public class User {
    private String ma , ten , gioitinh , email , sdt , diachi ;

    public User() {
    }

    public User(String ma, String ten, String gioitinh, String email, String sdt, String diachi) {
        this.ma = ma;
        this.ten = ten;
        this.gioitinh = gioitinh;
        this.email = email;
        this.sdt = sdt;
        this.diachi = diachi;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getGioitinh() {
        return gioitinh;
    }

    public void setGioitinh(String gioitinh) {
        this.gioitinh = gioitinh;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSdt() {
        return sdt;
    }

    public void setSdt(String sdt) {
        this.sdt = sdt;
    }

    public String getDiachi() {
        return diachi;
    }

    public void setDiachi(String diachi) {
        this.diachi = diachi;
    }
    
    
}
